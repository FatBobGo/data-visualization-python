/**
 * DataViz — Main Application Controller
 * Coordinates upload → parse → chart flow and manages application state.
 * Includes inline column header editing for no-header datasets.
 */

const App = (() => {
    // Application state
    const state = {
        sessionId: null,
        profile: null,
        recommendations: [],
        currentChart: null,
    };

    /**
     * Show a toast notification.
     */
    function showToast(message, type = 'info') {
        const container = document.getElementById('toast-container');
        const toast = document.createElement('div');
        toast.className = `toast toast-${type}`;
        toast.textContent = message;
        container.appendChild(toast);
        setTimeout(() => {
            toast.style.opacity = '0';
            toast.style.transform = 'translateX(100px)';
            toast.style.transition = '0.3s ease';
            setTimeout(() => toast.remove(), 300);
        }, 4000);
    }

    /**
     * Show loading overlay.
     */
    function showLoading(show) {
        const overlay = document.getElementById('loading-overlay');
        overlay.style.display = show ? 'flex' : 'none';
    }

    /**
     * Handle successful data parsing response.
     */
    function handleDataResponse(data) {
        state.sessionId = data.session_id;
        state.profile = data.profile;
        state.recommendations = data.recommendations;

        // Show data preview
        renderDataPreview(data.profile);

        // Show chart gallery
        ChartModule.renderGallery(data.recommendations);

        showToast(`Loaded ${data.profile.row_count} rows × ${data.profile.columns.length} columns`, 'success');
    }

    /**
     * Check if column names look auto-generated (e.g., Column_1, Column_2).
     */
    function hasAutoGeneratedHeaders(profile) {
        return !profile.has_header && profile.columns.some(c => /^Column_\d+$/.test(c.name));
    }

    /**
     * Render the data preview table with editable headers for no-header data.
     */
    function renderDataPreview(profile) {
        const section = document.getElementById('data-preview-section');
        const thead = document.getElementById('data-table-head');
        const tbody = document.getElementById('data-table-body');
        const info = document.getElementById('data-info');
        const banner = document.getElementById('header-edit-banner');

        section.style.display = 'block';
        section.classList.add('fade-in');

        info.textContent = `${profile.row_count} rows × ${profile.columns.length} columns`;

        const isEditable = hasAutoGeneratedHeaders(profile);
        banner.style.display = isEditable ? 'flex' : 'none';

        // Header row with type badges (editable if auto-generated)
        let headerHtml = '<tr>';
        profile.columns.forEach((col, idx) => {
            const badgeClass = `col-type-${col.dtype}`;
            const editIcon = isEditable
                ? '<svg class="header-edit-icon" width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M11 4H4a2 2 0 0 0-2 2v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2v-7"/><path d="M18.5 2.5a2.121 2.121 0 0 1 3 3L12 15l-4 1 1-4 9.5-9.5z"/></svg>'
                : '';
            const editableClass = isEditable ? ' editable-header' : '';
            const clickHandler = isEditable ? ` onclick="App.startHeaderEdit(this, ${idx}, '${escapeAttr(col.name)}')"` : '';
            headerHtml += `<th class="${editableClass}" data-col-idx="${idx}"${clickHandler}>${escapeHtml(col.name)}${editIcon}<span class="col-type-badge ${badgeClass}">${col.dtype}</span></th>`;
        });
        headerHtml += '</tr>';
        thead.innerHTML = headerHtml;

        // Data rows
        let bodyHtml = '';
        const rows = profile.preview_rows.slice(0, 50);
        rows.forEach(row => {
            bodyHtml += '<tr>';
            profile.columns.forEach(col => {
                const val = row[col.name];
                bodyHtml += `<td>${val !== null && val !== undefined ? escapeHtml(String(val)) : '<span style="color:var(--text-muted)">null</span>'}</td>`;
            });
            bodyHtml += '</tr>';
        });
        tbody.innerHTML = bodyHtml;

        // Populate controls
        Controls.populateColumns(profile.columns);
    }

    /**
     * Start inline editing of a column header.
     */
    function startHeaderEdit(thEl, colIdx, currentName) {
        // Don't re-enter if already editing
        if (thEl.querySelector('.header-input')) return;

        const input = document.createElement('input');
        input.type = 'text';
        input.className = 'header-input';
        input.value = currentName;
        input.dataset.colIdx = colIdx;
        input.dataset.origName = currentName;

        // Replace cell content with input
        thEl.innerHTML = '';
        thEl.appendChild(input);
        input.focus();
        input.select();

        // Commit on Enter, cancel on Escape, commit on blur
        input.addEventListener('keydown', (e) => {
            if (e.key === 'Enter') {
                e.preventDefault();
                commitHeaderEdit(input);
            } else if (e.key === 'Escape') {
                e.preventDefault();
                cancelHeaderEdit(input);
            }
        });

        input.addEventListener('blur', () => {
            // Small delay to allow click events to fire first
            setTimeout(() => {
                if (document.body.contains(input)) {
                    commitHeaderEdit(input);
                }
            }, 100);
        });
    }

    /**
     * Commit a column rename via the API.
     */
    function commitHeaderEdit(input) {
        const newName = input.value.trim();
        const origName = input.dataset.origName;

        if (!newName || newName === origName) {
            cancelHeaderEdit(input);
            return;
        }

        const formData = new FormData();
        formData.append('session_id', state.sessionId);
        formData.append('operation', 'rename_column');
        formData.append('column', origName);
        formData.append('new_name', newName);

        fetch('/api/transform', {
            method: 'POST',
            body: formData,
        })
            .then(res => {
                if (!res.ok) return res.json().then(data => { throw new Error(data.detail || 'Rename failed'); });
                return res.json();
            })
            .then(data => {
                // Update state and re-render
                state.profile = data.profile;
                state.recommendations = data.recommendations;
                renderDataPreview(data.profile);
                ChartModule.renderGallery(data.recommendations);
                showToast(`Renamed "${origName}" → "${newName}"`, 'success');
            })
            .catch(err => {
                showToast(err.message, 'error');
                cancelHeaderEdit(input);
            });
    }

    /**
     * Cancel header editing and restore original content.
     */
    function cancelHeaderEdit(input) {
        // Re-render the preview to restore the header
        if (state.profile) {
            renderDataPreview(state.profile);
        }
    }

    /**
     * Escape HTML entities.
     */
    function escapeHtml(text) {
        const div = document.createElement('div');
        div.textContent = text;
        return div.innerHTML;
    }

    /**
     * Escape text for use in HTML attributes.
     */
    function escapeAttr(text) {
        return text.replace(/'/g, "\\'").replace(/"/g, '&quot;');
    }

    /**
     * Apply batch column headers from comma-separated input.
     */
    function applyBatchHeaders() {
        const input = document.getElementById('batch-header-input');
        const value = input.value.trim();
        if (!value) {
            showToast('Please enter column names separated by commas', 'error');
            return;
        }

        const colCount = state.profile ? state.profile.columns.length : 0;
        const names = value.split(',').map(s => s.trim());

        if (names.length !== colCount) {
            showToast(`Expected ${colCount} column names, got ${names.length}`, 'error');
            return;
        }

        if (names.some(n => !n)) {
            showToast('Column names cannot be empty', 'error');
            return;
        }

        showLoading(true);

        const formData = new FormData();
        formData.append('session_id', state.sessionId);
        formData.append('headers', value);

        fetch('/api/batch-rename', {
            method: 'POST',
            body: formData,
        })
            .then(res => {
                if (!res.ok) return res.json().then(data => { throw new Error(data.detail || 'Batch rename failed'); });
                return res.json();
            })
            .then(data => {
                showLoading(false);
                state.profile = data.profile;
                state.recommendations = data.recommendations;
                renderDataPreview(data.profile);
                ChartModule.renderGallery(data.recommendations);
                showToast(`Renamed ${names.length} columns successfully`, 'success');
                // Clear the input
                input.value = '';
            })
            .catch(err => {
                showLoading(false);
                showToast(err.message, 'error');
            });
    }

    // Public API
    return {
        state,
        showToast,
        showLoading,
        handleDataResponse,
        renderDataPreview,
        escapeHtml,
        startHeaderEdit,
        applyBatchHeaders,
    };
})();

// --- Tab switching ---
document.addEventListener('DOMContentLoaded', () => {
    const tabs = document.querySelectorAll('.tab-btn');
    tabs.forEach(btn => {
        btn.addEventListener('click', () => {
            tabs.forEach(t => t.classList.remove('active'));
            document.querySelectorAll('.tab-content').forEach(c => c.classList.remove('active'));
            btn.classList.add('active');
            document.getElementById(btn.dataset.tab).classList.add('active');
        });
    });

    // Batch header apply button
    document.getElementById('btn-apply-batch-headers').addEventListener('click', App.applyBatchHeaders);

    // Allow Enter key in batch header input
    document.getElementById('batch-header-input').addEventListener('keydown', (e) => {
        if (e.key === 'Enter') {
            e.preventDefault();
            App.applyBatchHeaders();
        }
    });

    // Load sample data list
    fetch('/api/sample-data')
        .then(res => res.json())
        .then(samples => {
            const grid = document.getElementById('sample-grid');
            samples.forEach(s => {
                const card = document.createElement('div');
                card.className = 'sample-card';
                card.innerHTML = `
                    <div class="sample-card-title">${App.escapeHtml(s.name)}</div>
                    <div class="sample-card-desc">${App.escapeHtml(s.description)}</div>
                `;
                card.addEventListener('click', () => {
                    App.showLoading(true);
                    fetch(`/api/sample-data/${s.id}`, { method: 'POST' })
                        .then(res => res.json())
                        .then(data => {
                            App.showLoading(false);
                            App.handleDataResponse(data);
                        })
                        .catch(err => {
                            App.showLoading(false);
                            App.showToast('Failed to load sample data: ' + err.message, 'error');
                        });
                });
                grid.appendChild(card);
            });
        })
        .catch(() => {});
});
